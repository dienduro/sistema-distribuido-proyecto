    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtNombre.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite Su nombtr ");
            txtNombre.requestFocus();
            return;
            
        }
        if (txtDireccion.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite Su direccion ");
            txtDireccion.requestFocus();
            return;
            
        }
        if (txtTelefono.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite Su telefono ");
            txtTelefono.requestFocus();
            return;
            
        }
        if (cmbEmpresa.getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite Su telefono ");
            cmbEmpresa.requestFocus();
            return;
        }
        // TODO add your handling code here:
        Proveedores objProveedor = new Proveedores();
        String nombres = txtNombre.getText();
        String direccion = txtDireccion.getText();
        String telefono = txtTelefono.getText();
        int empresa_ruc = cmbEmpresa.getSelectedIndex();
        
        int ruc_prov = Integer.parseInt(txtProvRuc.getText());
        if (ruc_prov == 0) {
            
            boolean resultado = objProveedor.insertarProveedor(nombres, direccion, telefono, empresa_ruc);
            if (resultado == true) {
                JOptionPane.showMessageDialog(null, "Se inserto un nuevo registro");
                modeloTablaProv.setNumRows(0);                
                cargarTablaproveedor();
            } else {
                JOptionPane.showMessageDialog(null, "Erro al inserta su datos ");
            }
        } else {
            boolean resultado = objProveedor.actualizarProveedor(ruc_prov, nombres, direccion, telefono, empresa_ruc);
            if (resultado == true) {
                JOptionPane.showMessageDialog(null, "Se actualizo el registro");
                //cargarTabla();
            } else {
                JOptionPane.showMessageDialog(null, "Error al actualizar  ");
            }
        }
        // *** Limpio los Campos ***  
        txtProvRuc.setText("0");
        txtNombre.setText("");
        txtDireccion.setText("");
        txtTelefono.setText("");
        txtNombre.requestFocus();        // TODO add your handling code here:
    }//GEN-LAST:event_btnGuardarActionPerformed

